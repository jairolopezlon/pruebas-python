def buscarPatron(img, pat):

    iw = len(img[0])
    ih = len(img)
    pw = len(pat[0])
    ph = len(pat)
    cox = -1
    coy = -1
    co = [-1, -1]

    for i in img:
        coy += 1
        for j in pat:

            hasLenght = ph <= ih - (coy + 1)
            isPattern = i.find(j) != -1
            isFirstLine = pat[0] == j

            if isPattern and isFirstLine and hasLenght:
                cox = i.find(j)

                if ph == 1:
                    co = [cox, coy]
                    return co
                else:
                    for index in range(1, ph):

                        isNextPat = img[coy+index].find(pat[index]) != -1
                        isCox = img[coy+index].find(pat[index]) == cox

                        if not isNextPat and not isCox:
                            break
                        else:
                            if index == (ph - 1):
                                co = [cox, coy]
                                return co

            elif not hasLenght:
                return co
            else:
                break
            break
    return co


